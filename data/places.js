function buildLocalCandidate(region, entry, index) {
  const center = REGION_CENTERS[region];
  const [name, category, extraTags = []] = entry;
  const preset = CATEGORY_PRESETS[category] || CATEGORY_PRESETS.자연;
  const latOffset = ((index % 9) - 4) * 0.008 + Math.floor(index / 9) * 0.002;
  const lngOffset = ((index % 7) - 3) * 0.009 - Math.floor(index / 7) * 0.002;
  const baseCount = window.PLACES.filter((place) => place.region === region).length;
  return {
    id: `${center.prefix}_${String(baseCount + index + 1).padStart(2, "0")}`,
    name,
    region,
    category,
    lat: Number((center.lat + latOffset).toFixed(4)),
    lng: Number((center.lng + lngOffset).toFixed(4)),
    duration: preset.duration,
    indoor: preset.indoor,
    outdoor: preset.outdoor,
    parking: preset.parking,
    baby: preset.baby,
    pet: extraTags.includes("pet") ? true : preset.pet,
    slots: preset.slots,
    tags: [...new Set([...preset.tags, ...extraTags])],
    scores: preset.scores,
    description: `${region} 여행에서 ${category} 성향과 ${[...new Set([...preset.tags, ...extraTags])].slice(0, 3).join(", ")} 취향을 반영하기 위한 로컬 후보입니다.`
  };
}

Object.entries(LOCAL_PLACE_CANDIDATES).forEach(([region, entries]) => {
  const existingNames = new Set(window.PLACES.filter((place) => place.region === region).map((place) => place.name));
  const needed = Math.max(0, 50 - existingNames.size);
  const extras = entries
    .filter(([name]) => !existingNames.has(name))
    .slice(0, needed)
    .map((entry, index) => buildLocalCandidate(region, entry, index));
  window.PLACES.push(...extras);
});
